@client.event
async def on_ready():
    print(f'{client.user} has connected to Discord!')

@client.command(pass_context=True)
async def hello(ctx):
    await ctx.send('Hello')

@client.command(pass_context=True)
async def t(ctx):
    command = ctx.message.content.split(" ")
    lang = command[1].lower()
    text = command[2:]
    result = translate(lang,text)
    if lang not in supported_codes and lang not in supported_languages:
        result = " This Language is not available."
    result = result.replace("[","")
    result = result.replace("]","")
    result = result.replace(","," ")
    result = result.replace("'","")
    await ctx.send(f'{ctx.message.author.mention}' + f' {result}')

client.run(token)
